var angka = parseInt(prompt("Masukan Angka"));

switch(angka){
    case 80 :
        alert("Nilai Anda A");
        break;
    case 70 :
        alert("Nilai Anda B");
        break;
    case 50 :
        alert("Nilai Anda C");
        break;
    default :
        alert("Nilai Anda D");
        break;
}
